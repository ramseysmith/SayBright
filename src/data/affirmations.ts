function buildAffirmations(
  categoryId: string,
  texts: string[],
  isPremium: boolean
): Affirmation[] {
  return texts.map((text, index) => ({
    id: `${categoryId}_${String(index + 1).padStart(2, '0')}`,
    text,
    categoryId,
    isPremium,
  }));
}

